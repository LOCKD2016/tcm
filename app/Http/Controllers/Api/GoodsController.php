    // 获取所有的商品信息
    public function getlist()
    {
        $list = DB::table('goods')->select('id', 'goods_name', 'goods_price')->orderBy('id', 'asc')->get();
        foreach ($list as $k => $v) {
            if ($v->id > 1) {
                $list[$k]->goods_name = $v->goods_name . '(' . $v->goods_price . '元)';
            }
        }
        return $this->response()->array($list);
    }

